/** 生成一条编辑阶段随机弹幕（等待/催促型） */
export function generateEditingDanmaku(): string {
  // 10%概率混入短词
  if (Math.random() < 0.10) {
    return randomShortWord();
  }
  const template = EDITING_TEMPLATES[Math.floor(Math.random() * EDITING_TEMPLATES.length)];
  return fillProps(template);
}

/** 生成一条过渡阶段随机弹幕（点击开始瞬间爆发） */
export function generateTransitionDanmaku(): string {
  // 60%概率是短词刷屏，40%是兴奋反应类
  if (Math.random() < 0.60) {
    return TRANSITION_SHORT[Math.floor(Math.random() * TRANSITION_SHORT.length)];
  }
  return TRANSITION_SHORT[Math.floor(Math.random() * TRANSITION_SHORT.length)];
}

/** 生成一条表演阶段随机弹幕（活人感） */
export function generatePresetDanmaku(): string {
  // 20%概率混入短词，模拟直播间刷屏
  if (Math.random() < 0.20) {
    return randomShortWord();
  }
  const template = PERFORM_TEMPLATES[Math.floor(Math.random() * PERFORM_TEMPLATES.length)];
  return fillProps(template);
}

/** 生成N条不重复的随机弹幕 */
export function generatePresetDanmakuBatch(count: number): string[] {
  const shuffled = [...PERFORM_TEMPLATES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length)).map(fillProps);
}

/** 获取原始模板数量 */
export function getPresetDanmakuCount(): number {
  return PERFORM_TEMPLATES.length + EDITING_TEMPLATES.length + TRANSITION_SHORT.length;
}