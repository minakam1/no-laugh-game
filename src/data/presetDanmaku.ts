import { PROP_MANIFEST } from '@/phaser/assets/manifest';

/** 所有道具中文名列表 */
const PROP_NAMES: string[] = Object.values(PROP_MANIFEST).map((p) => p.label);

/** 按分类的道具名，用于语义匹配 */
const PROP_BY_TAG: Record<string, string[]> = {
  滑倒: ['香蕉皮', '胶水地毯'],
  爆炸: ['定时炸弹', '爆炸桶'],
  传送: ['传送门', '弹射板', '喷气背包'],
  弹跳: ['弹跳蘑菇', '蹦床', '弹射板'],
  吸附: ['磁铁地板'],
  烟雾: ['烟雾机'],
  镜像: ['镜子'],
  旋转: ['旋转舞台'],
  重力: ['反向重力区'],
  掉落: ['假天花板'],
  飞行: ['喷气背包', '弹射板'],
  骑行: ['自行车', '滑板'],
  液体: ['咖啡杯'],
  拳击: ['弹簧拳套'],
  人物: ['呆萌NPC'],
  吹风: ['吹风机'],
};

/** 随机取一个道具名 */
function randomProp(): string {
  return PROP_NAMES[Math.floor(Math.random() * PROP_NAMES.length)];
}

/** 随机取一个指定分类的道具名 */
function randomPropByTag(tag: string): string {
  const list = PROP_BY_TAG[tag] || PROP_NAMES;
  return list[Math.floor(Math.random() * list.length)];
}

/** 替换模板中的 {道具} 占位符 */
function fillProps(template: string): string {
  return template
    .replace(/{道具A}/g, randomProp)
    .replace(/{道具B}/g, randomProp)
    .replace(/{滑倒类}/g, () => randomPropByTag('滑倒'))
    .replace(/{爆炸类}/g, () => randomPropByTag('爆炸'))
    .replace(/{传送类}/g, () => randomPropByTag('传送'))
    .replace(/{弹跳类}/g, () => randomPropByTag('弹跳'))
    .replace(/{飞行类}/g, () => randomPropByTag('飞行'))
    .replace(/{骑行类}/g, () => randomPropByTag('骑行'))
    .replace(/{道具}/g, randomProp);
}

