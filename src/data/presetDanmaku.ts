/** 随机取一个道具名 */
function randomProp(): string {
  return PROP_NAMES[Math.floor(Math.random() * PROP_NAMES.length)];
}

/** 随机取一个指定分类的道具名 */
function randomPropByTag(tag: string): string {
  const list = PROP_BY_TAG[tag] || PROP_NAMES;
  return list[Math.floor(Math.random() * list.length)];
}

/** 替换模板中的 {道具} 占位符（优化版：使用 split/join 替代多次 regex） */
function fillProps(template: string): string {
  // 预生成所有需要的替换值
  const replaceMap: Record<string, string> = {
    '{道具A}': randomProp(),
    '{道具B}': randomProp(),
    '{滑倒类}': randomPropByTag('滑倒'),
    '{爆炸类}': randomPropByTag('爆炸'),
    '{传送类}': randomPropByTag('传送'),
    '{弹跳类}': randomPropByTag('弹跳'),
    '{飞行类}': randomPropByTag('飞行'),
    '{骑行类}': randomPropByTag('骑行'),
  };

  let result = template;
  for (const [placeholder, replacement] of Object.entries(replaceMap)) {
    if (result.includes(placeholder)) {
      result = result.split(placeholder).join(replacement);
    }
  }
  // 处理通用 {道具}（每次出现可能不同）
  if (result.includes('{道具}')) {
    const parts = result.split('{道具}');
    result = parts[0];
    for (let i = 1; i < parts.length; i++) {
      result += randomProp() + parts[i];
    }
  }
  return result;
}

