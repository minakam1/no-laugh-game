/** 生成一条随机弹幕（已填充道具） */
export function generatePresetDanmaku(): string {
  const template = DANMAKU_TEMPLATES[Math.floor(Math.random() * DANMAKU_TEMPLATES.length)];
  return fillProps(template);
}

/** 生成N条不重复的随机弹幕 */
