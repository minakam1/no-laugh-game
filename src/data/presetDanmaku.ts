/** 检查文本是否为攻击性弹幕 */
export function isOffensiveDanmaku(text: string): boolean {
  return OFFENSIVE_TEMPLATES.some((t) => {
    const plain = t.replace(/\{[^}]+\}/g, '');
    return text.includes(plain) || OFFENSIVE_TEMPLATES.includes(text);
  });
}

/** 随机获取一条攻击性弹幕 */
export function randomOffensive(): string {
  return OFFENSIVE_TEMPLATES[Math.floor(Math.random() * OFFENSIVE_TEMPLATES.length)];
}

