/** 表演阶段弹幕模板（第1-10组，索引0-199） */
const PERFORM_TEMPLATES = DANMAKU_TEMPLATES.slice(0, 200);

/** 编辑阶段弹幕模板（第11-15组，索引200-299） */
const EDITING_TEMPLATES = DANMAKU_TEMPLATES.slice(200);

/** 生成一条表演阶段随机弹幕（已填充道具） */
export function generatePresetDanmaku(): string {
  const template = PERFORM_TEMPLATES[Math.floor(Math.random() * PERFORM_TEMPLATES.length)];
  return fillProps(template);
}

/** 生成一条编辑阶段随机弹幕（等待/催促/期待型，已填充道具） */
export function generateEditingDanmaku(): string {
  const template = EDITING_TEMPLATES[Math.floor(Math.random() * EDITING_TEMPLATES.length)];
  return fillProps(template);
}

/** 生成N条不重复的随机弹幕 */
export function generatePresetDanmakuBatch(count: number): string[] {
  const shuffled = [...DANMAKU_TEMPLATES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length)).map(fillProps);
}

/** 获取原始模板数量 */
export function getPresetDanmakuCount(): number {
  return DANMAKU_TEMPLATES.length;
}
